#include <stdio.h>
void main
{
  // syntax:-  scanf("format specifiers",&variable_name);
  
  
  /*  format specifiers are :-
  
      %d to accept input of integers.

      %ld to  accept input of long integers

      %lld to accept input of long long integers

      %f to accept input of real number.

      %c to accept input of character types.

      %s to accept input of a string.         */
  
  
  scanf("%d",&int_variable); //to take input for integer variable  ex. 1, 2, 3, 4, ....
  
  
  scanf("%f",&float_variable); //to take input for float variable ex. 10.4, 1.5, 3.142, ....
  
  
  scanf("%c",&char_variable); //to take input for character variable  ex. a, b, c, d, ....
  
  
  scanf("%s",&string_variable); //to take input for string variable ex. name, address, text, ....
  
  
}
